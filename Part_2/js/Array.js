for (let i = 0; i <= arr2.length - 1; i++) {
    for (let j = 0; j <= arr2.length - 1; j++) {
        let max;
        if (arr2[j] > arr2[j + 1]) {
            max = arr2[j + 1];
            arr2[j + 1] = arr2[j];
            arr2[j] = max;
        }
    }
}
