let arr2 = ['լյուն', 'բեն', 'այբ', 'գիմ', 'եչ', 'է', 'ըթ', 'թո', 'ինի', 'ժե', 'հո', 'ղատ', 'ճե', 'մեն', 'դա', ];

/*Արտածել  5֊րդ և 8֊րդ տառերի անունները։*/

console.log(`${arr2[4]}, ${arr2[7]}`);
